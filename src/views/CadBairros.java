    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
 try{
            boolean retorno;
            retorno = validarDados();
            
            if(retorno) {
                
                objBairro = new Bairro();
                objBairro.setNome(txtNome.getText());
                Combos objCombo = (Combos) jcbCidade.getSelectedItem();
                String id_cidade = objCombo.getCodigo();
                objBairro.setId_cidade(Integer.parseInt(id_cidade));
                BairroController objBairroController = new BairroController();
                
             if (lblId.getText().equals("ID")){
                retorno = objBairroController.incluir(objBairro);
            }else {
                    objBairro.setId(Integer.parseInt(lblId.getText()));
                    retorno = objBairroController.alterar(objBairro);
                }
             
             if (retorno == false){
                 CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao incluir");
             } else {
                 limparTela();
             }
                
            }

            atualizarTabela();

        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao tentar incluir");
            System.out.println("ERRO: " + ex.getMessage().toString());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

